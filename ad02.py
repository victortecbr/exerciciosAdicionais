# 1.Peça dois números, calcule e mostre:
# o	soma
# o	subtração
# o	multiplicação
# o	divisão

print("-- Digite dois numeros e obtenha o resultado das quatro operações: (+, -, *, /) --")
num1 = float(input("Digite o primeiro numero: "))
num2 = float(input("Digite o segundo numero: "))

print(f"soma {num1} + {num2} = {num1 + num2}")
print(f"soma {num1} - {num2} = {num1 - num2}")
print(f"soma {num1} * {num2} = {num1 * num2}")
print(f"soma {num1} / {num2} = {num1 / num2}")